def sum(x, y):
    """ Calculate sum
    Parameters: 
        x (int): a number
        y (int): a number
    Returns: 
        int: sum of x and y
    """
    return x+y

def average(x, y):
    """ Calculate average
    Parameters: 
        x (int): a number
        y (int): a number
    Returns: 
        double: average of x and y
    """
    return (x+y)/2

def power(x, y):
    """ Calculate power
    Parameters: 
        x (int): a number
        y (int): a number
    Returns: 
        int: x to the power of y
    """
    return x**y
